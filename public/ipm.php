			<h4>1. Remove Mosquito Habitats</h4>
			
			<p>An important part of mosquito control around homes is making sure that mosquitoes don't have a place to lay their eggs.
			Because mosquitoes need water for two stages of their life cycle, it's important to monitor standing water sources.
			</p>
			
			<img id="flood-irrigation-image" src="images/flooded_field.jpg" alt="flood irrigating pastures" />
			
			<ul>
				<li>Get rid of standing water in rain gutters, old tires, buckets, plastic covers, toys or any other container where
				mosquitoes can breed. </li>
				<li>Empty and change the water in bird baths, fountains, wading pools, rain barrels and potted plant trays at least once a
				week to eliminate potential mosquito habitats. </li>
				<li>Drain temporary pools of water or fill with dirt. </li>
				<li>Keep swimming pool water treated and circulating. </li>
				<li>If flood irrigating, do not over water and make sure water does not stand for long during warm weather. Mosquito eggs
				can hatch and larva can develop to pupa and then adults in just a few days. </li>
			</ul>

