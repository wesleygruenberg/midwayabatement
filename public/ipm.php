			management techniques that focus on pest prevention, pest reduction, and the elimination of conditions that lead to pest
			infestations. IPM programs also rely heavily on resident education and pest monitoring.A successful IPM strategy can use pesticides.
			</p>
			
			<p>IPM uses a combination of ways to control mosquito populations with decisions based on surveillance, such as keeping track or
			count of the numbers and types of mosquitoes in an area. Surveillance is a critical component to any successful IPM program
			because the results from the surveillance will help determine the appropriate response to an infestation. Extensive infestations, or
			those where disease is present, merit a different response than will lower levels of infestations.Both CDC and EPA recognize a
			legitimate and compelling need for the use of chemical interventions, under certain circumstances, to control adult mosquitoes. This
			is especially true during periods of mosquitoborne
			disease transmission or when source reduction and larval control have failed or
			are not feasible.
			</p>
			
