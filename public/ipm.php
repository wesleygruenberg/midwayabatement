			<h4>3. Control Mosquitoes at the Larval Stage</h4>
			<img id="dipper-cup-image" src="images/larvae_dipping.jpg" alt="larva in dipper cup">
			<p>The greatest impact on mosquito populations will occur when they are concentrated, immobile and accessible. This
			emphasis focuses on habitat management and controlling the immature stages (egg, larva, and pupa) before the
			mosquitoes emerge as adults. This approach maximizes the effectiveness of pesticide application and minimizes the use
			from widespread pesticide application. Larvicides target larvae in the breeding habitat before they can mature into adult
			mosquitoes and disperse. Larvicide treatment of breeding habitats helps reduce the adult mosquito population in nearby
			areas. </p>
			
			<p>Culex pipiens mosquitoes can use natural locations or habitats (for example tree holes and crevices in plants) and
			artificial containers with water to lay their eggs. They lay eggs during the day in water containing organic material (e.g.,
			decaying leaves, algae, etc.) in containers with wide openings. They prefer darkcolored
			containers located in the shade.
			Other sites where they may lay their eggs include: old tires, buckets, toys, potted plant trays and saucers, plastic covers
			and even places as small as bottle caps. </p>
			<img id="bp-image" src="images/backpack.jpg" alt="backpack larvicide application"/>

			<p>Egg and larva interventions are generally the most effective, least costly, way to control mosquitoes. However, these
			interventions are unlikely to be 100% effective, especially for mosquitoes like the Culex pipiens that breed in varied and
			scattered locations. In these cases, eliminating or treating all or even most standing water can be nearly impossible.
			Successful control efforts will need to supplement habitat removal with other means of control.Involvement of the
			community is essential to these interventions. Residents, neighbors, and landlords can all be proactive in eliminating
			standing water or alerting others to its presence to eliminate even the smallest sources of standing water. </p>
			<p>There are a number of EPAregistered active ingredients used in larvicides. Choosing which larvicide to use in a given
			area is best done by experts and will depend on a variety of factors, including potential human or environmental risk,
			cost, resistance, and ease of use. </p>

