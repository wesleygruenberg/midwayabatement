		<section id="adult-control">
			<h4>4. Controlling Mosquitoes at the Adult Stage</h4>
			<img id = "truck-spray-image" src="images/truck_spray_ipm.jpg" alt="Mosquito Spray Truck" />

			<p>Using an EPA registered
			pesticide is one of the fastest and best options to combat an outbreak of mosquitoborne
			disease
			being transmitted by adult mosquitoes. The pesticides registered for this use are known as adulticides. Adulticides are
			applied either using aerial applications by aircraft or on the ground by truckmounted
			sprayers. </p>
			<p>Aerial spraying techniques can treat large areas with only small amounts of pesticide and have been used safely for more
			than 50 years. These aerial sprays are been fully evaluated by EPA and don’t pose risks to people or the environment
			when used according to the directions on the label. Mosquito adulticides are applied as ultralow
			volume (ULV) sprays.
			ULV sprayers dispense extremely small droplets. The Duet insecticide, for example, uses 80 microns or less which means
			hundreds of thousands of droplets could fit inside something as small as one pea. The small droplet size makes the
			pesticide more effective, which means less pesticide is used to better protect people and the environment. </p>
			<p>Extensive scientific research has been conducted by academia, industry, and government agencies to identify appropriate
			droplet sizes for individual compounds. The equipment nozzles undergo rigorous testing before being sold to the mosquito
			controllers. ULV applications involve very small quantities of pesticide active ingredient in relation to the size of the area
			treated. </p>
			<p>There are a number of registered adulticides to choose from. Choosing which adulticide to use in a given area is a job best
			done by experts and will depend on a variety of factors such as the type of mosquito, whether the mosquitoes are resistant
			to particular types of pesticides, weather, etc. </p>
