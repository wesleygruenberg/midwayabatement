			<h4>2. Use Structural Barriers</h4>

			<p>Because mosquitoes frequently bite indoors, using structural barriers is an important way to reduce the incidence of bites.
			Examples of structural barriers include:</p>
			<ul>
				<li>Install window and door screens if they are not already in place.</li>
				<li>Cover all gaps in walls, doors and windows to prevent mosquitoes from entering. </li>
				<li>Make sure window and door screens are "bug tight." </li>
				<li>Completely cover baby carriers and beds with netting. Nets can be especially important for protecting a sick person from
				getting more mosquito bites, which could transmit the disease to other people. </li>
			</ul>

