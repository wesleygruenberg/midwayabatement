			<img class="mosquito101-image" id="pupa-image" src="https://c1.staticflickr.com/1/44/151533744_c35a63b1d5_z.jpg?zz=1" alt="Pupal stage"/>
			
		
			<h4>Pupa Stage</h4>
			<p>Mosquito pupae, commonly called "tumblers," live in water from 1 to 4 days, depending upon species and temperature.</p>
			<p>The pupa is lighter than water and therefore floats at the surface. It takes oxygen through two breathing tubes called 
			"trumpets." The pupa does not eat, but it is not an inactive stage. When disturbed, it dives in a jerking, tumbling motion 
			toward protection and then floats back to the surface.</p>
			<p>The metamorphosis of the mosquito into an adult is completed within the pupal case. The pupal case thus serves as a factory wherein 
			the mosquito makes an adult out of a larva. The adult mosquito splits the pupal case and emerges to the surface of the water where 
			it rests until its body dries and hardens</p>
