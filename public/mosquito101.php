			<img class="mosquito101-image" id="larva-image" src="http://infinitespider.com/wp-content/uploads/2014/05/Culex_sp_larvae-mosquito-larvae-wiki.png" alt="Mosquito larva"/>
			
			<h4>Larval Stage</h4>
			
			<p>Mosquito larvae, commonly called "wigglers," live in water from 4 to 14 days depending on water temperature.</p>

			<p>Larvae of almost all species must come to the surface at frequent intervals to obtain oxygen through a breathing 
			tube called a siphon. Larvae of Coquillettidia and Mansonia possess modified siphons that allow them to pierce the stems 
			of emergent vegetation in water and draw their oxygen from the plant in this process.  Larvae are constantly feeding 
			since maturation requires a huge amount of energy and food. They hang with their heads down and the brushes by their 
			mouths filtering anything small enough to be eaten toward their mouths to nourish the growing larvae. They feed on 
			algae, plankton, fungi and bacteria and other microorganisms. They breath at the water surface with the breathing 
			tube up breaking the water surface tension. The larvae of a few mosquito species are cannibalistic, feeding on larvae 
			of other mosquitoes: Toxorhynchites and some Psorophora, the largest mosquitoes known, are predators of other mosquito 
			larvae sharing their habitat. Their larvae are much larger than other mosquito larvae.</p>

			<p>During growth, the larva molts (sheds its skin) four times. The stages between molts are called instars. At the 4th 
			instar, the usual larva reaches a length of almost 1/2 inch and toward the end of this instar ceases feeding. When the 
			4th instar larva molts, it becomes a pupa.</p>

