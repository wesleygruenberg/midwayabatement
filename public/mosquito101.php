			<h4>Egg Stage</h4>
		
			<p>Many mosquitoes, such as <a href="https://en.wikipedia.org/wiki/Culex_pipiens">Culex pipiens</a>, lay their eggs on 
			the surface of fresh or stagnant water. 
			The water may be in tin cans, barrels, horse troughs, ornamental ponds, swimming pools, puddles, creeks, ditches, 
			catch basins or marshy areas. Mosquitoes prefer water sheltered from the wind by grass and weeds.</p>

			<p><a href="https://en.wikipedia.org/wiki/Culex">Culex mosquitoes</a> usually lay their eggs at night over a period of time sticking them 
			together to form a 
			raft of from 100 to 300 eggs. A raft of eggs looks like a speck of soot floating on the water and is about 1/4 
			inch long and 1/8 inch wide. A female mosquito may lay a raft of eggs every third night during its life span.</p>

			<p>Anopheles and many other mosquitoes lay their eggs singly on the water surface. <a href="https://en.wikipedia.org/wiki/Aedes">Aedes</a> 
			and <a href="https://en.wikipedia.org/wiki/Ochlerotatus">Ochlerotatus</a>  mosquitoes 
			lay their eggs singly, usually on damp soil.  Aedes and Ochlerotatus eggs are more resistant to drying out (some 
			require complete drying out before the eggs will hatch) and hatch only when flooded with water (salt water high tides, 
			irrigated pastures, treeholes flooded by rains, flooded stream bottoms).  <a href="https://en.wikipedia.org/wiki/Anopheles">Anopheles</a>, 
			Culex and <a href="https://en.wikipedia.org/wiki/Mansonia">Mansonia</a> eggs are 
			susceptible to drying out during extended droughts.</p>

			<p>Tiny mosquito larvae (1st instar) emerge from the eggs within 24 - 48 hours almost in unison.</p>
