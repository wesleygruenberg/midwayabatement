		<section id = "mosquito-adult-stage">
			<img class="mosquito101-image" id="adult-mosquito-image" src="https://www.montgomerycountymd.gov/Mosquito/Resources/Images/cdc-asian-tiger-mosquito.jpg" alt="Adult mosquito taking blood meal"/>
			
			<h4>Adult Stage</h4>
			<p>After breaking free of the pupal skin, the adult will rest on the water's surface for a brief period of 
			time to allow its body and wings to dry and harden. Once this has occurred the adult mosquito can now fly. 
			There are over 2,500 different species of mosquitoes in the world and about 20 different species within Midway Abatement 
			District. Different species will have different characteristics and behaviors. This affects things like; active 
			time period (e.g., daytime or nighttime biters), how far they fly from their hatching source, what types of 
			animals they "prefer" to bite (Example; some prefer birds over mammals) and other factors.</p>
			
			<p>Only female mosquitoes require a blood meal and bite animals - warm or cold blooded - and birds. Stimuli 
			that influence biting (blood feeding) include a combination of carbon dioxide, temperature, moisture, smell, 
			color and movement. Male mosquitoes do not bite, but feed on the nectar of flowers or other suitable sugar source. 
			Acquiring a blood meal (protein) is essential for egg production, but mostly both male and female mosquitoes 
			are nectar feeders for their nutrition. Female Toxorhynchites actually can't obtain a bloodmeal and are restricted 
			to a nectar diet. Of those female mosquitoes capable of blood feeding, human blood meals are seldom first or second 
			choices. Horses, cattle, smaller mammals and/or birds are preferred.</p>

			<p><a href="https://en.wikipedia.org/wiki/Aedes">Aedes</a> and <a href="https://en.wikipedia.org/wiki/Ochlerotatus">Ochlerotatus</a> 
			mosquitoes are painful and persistent biters. They search for a blood meal early in the 
			morning, at dusk (crepuscular feeders) and into the evening. Some are diurnal (daytime biters) especially on cloudy 
			days and in shaded areas. They usually do not enter dwellings, and they prefer to bite mammals like humans. Aedes and 
			Ochlerotatus mosquitoes are strong fliers and are known to fly many miles from their larval developments sites.</p>

			<p><a href="https://en.wikipedia.org/wiki/Culex">Culex</a> mosquitoes are painful and persistent biters also, but prefer 
			to attack at dusk and after dark. They readily 
			enter dwellings for blood meals. Domestic and wild birds usually are preferred over man, cows, and horses. Culex 
			nigripalpus is known to transmit St. Louis encephalitis to man in Florida. Culex mosquitoes are generally weak fliers 
			and do not move far from home, although they have been known to fly up to two miles, Culex usually live only a few 
			weeks during the warm summer months. Those females that emerge in late summer search for sheltered areas where they 
			"hibernate" until spring. Warm weather brings them out again in search of water on which to lay their eggs.</p>

			<p><a href="https://en.wikipedia.org/wiki/Culiseta">Culiseta</a> mosquitoes are moderately aggressive biters, attacking 
			in the evening hours or in the shade during the day. 
			<a href="https://en.wikipedia.org/wiki/Psorophora">Psorophora</a>, <a href="https://en.wikipedia.org/wiki/Coquillettidia">Coquillettidia</a>
			and <a href="https://en.wikipedia.org/wiki/Mansonia">Mansonia</a> mosquitoes are becoming more pestiferous as an ever-expanding human population 
			invades their natural habitats. <a href="https://en.wikipedia.org/wiki/Anopheles">Anopheles</a> mosquitoes are persistent biters and are the only mosquitoes which transmit 
			malaria to man.</p>

