			<h2>Mosquito Biology</h2>
			<p>
			Knowing the different stages of the mosquito's life will help you prevent mosquitoes
			around your home and also help you choose the right pesticides for your needs, if you
			decide to use them. All mosquito species go through four distinct stages during their life
			cycle: 
			</p>
			<img class="mosquito101-image" id="mosquito-lifecycle-image" src="images/lifecycle.jpg" alt="Mosquito Lifecycle">
			<ul>
				<li><a href="#mosquito-egg-stage">Egg</a> - hatches when exposed to water.</li>
				<li><a href="#mosquito-larval-stage">Larva</a> - "wriggler" lives in water, molts several times. Most species surface to breathe air.</li>
				<li><a href="#mosquito-pupa-stage">Pupa</a> - "tumbler" does not feed. This is the stage just before the mosquito emerges as an adult</li>
				<li><a href="#mosquito-adult-stage">Adult</a> - flies short time after emerging and after its body parts have hardened.</li>
			</ul>
			
			<p>The first three stages occur in water, but the adult is an active flying insect. Only the
			female mosquito bites and feeds on the blood of humans or other animals.
			</p>
			
			<ul>
				<li>After she obtains a blood meal, the female mosquito lays the eggs directly on or
				near water, soil and at the base of some plants in places that may fill with water.
				The eggs can survive dry conditions for a few months. </li>
				<li>The eggs hatch in water and a mosquito larva or "wriggler" emerges. The length of
				time to hatch depends on water temperature, food and type of mosquito. </li>
				<li>The larva lives in the water, feeds and develops into the third stage of the life cycle
				called, a pupa or "tumbler." The pupa also lives in the water but no longer feeds. </li>
				<li>Finally, the mosquito emerges from the pupal case after two days to a week in the
				pupal stage. </li>
			</ul>


