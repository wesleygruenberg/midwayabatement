		<h2>What Can You Do?</h2>
		<p>An efficient way to control mosquitoes is to find and eliminate their larval habitat. Eliminating large larval 
		development sites (source reduction) such as swamps or sluggishly moving streams or ditches may require community-wide 
		effort. This is usually a task for your organized mosquito control program.</p>
		
		<img id="breeding-sites-image" src="images/breeding-sites.png" alt="Common Breeding Sites">
		
		<p><strong>Homeowners</strong> can take the following steps to prevent mosquito breeding on their own property:</p>
		<div id="homeowner-checklist">
			<ol>
				<li><em>Destroyor dispose</em>  of tin cans, old tires, buckets, unused plastic swimming pools or other containers that 
				collect and hold water. Do not allow water to accumulate in the saucers of flowerpots, cemetery urns or in pet dishes 
				for more than 2 days.</li>
				<li><em>Clean</em> debris from rain gutters and remove any standing water under or around structures, or on flat roofs. Check 
				around faucets and air conditioner units and repair leaks or eliminate puddles that remain for several days.</li>
				<li><em>Change the water</em> in birdbaths and wading pools at least once a week and stock ornamental pools with top feeding 
				predacious minnows. Known as mosquito fish, these minnows are about 1 - 1-1/2 inches in length and can be purchased 
				or native fish can be seined from streams and creeks locally. Ornamental pools may be treated with biorational 
				larvicides [<em>Bacillus thuringiensis</em> subsp. <em>israelensis</em> (<em>Bti</em>) or S-methoprene (IGR) containing 
				products] under certain circumstances. Commercial products "Mosquito Dunks" and "Mosquito Bits" containing <em>Bti</em> 
				can be purchased at many hardware/garden stores for homeowner use. Wellmark International, a division of Central Life 
				Sciences, has developed Pre-Strike Mosquito Torpedo that kills developing mosquitoes using insect growth regulator (IGR) 
				technology. Like Mosquito Dunks, Pre-Strike can be found at many home/garden and pet specialty stores.</li>
				<li><em>Fill or drain puddles, ditches and swampy areas</em>, and either remove, drain or fill tree holes and stumps with mortar. 
				These areas may be treated with <em>Bti</em> or methoprene products also.</li>
				<li><em>Eliminate seepage</em>  from cisterns, cesspools, and septic tanks.</li>
				<li><em>Eliminate standing water</em>  around animal watering troughs. Flush livestock water troughs twice a week.</li>
				<li><em>Check for trapped water</em>  in plastic or canvas tarps used to cover boats, pools, etc. Arrange the tarp to drain the water.</li>
				<li>Check around construction sites or do-it-yourself improvements to ensure that proper backfilling and grading prevent 
				drainage problems.</li>
				<li><em>Irrigate lawns and gardens <strong>carefully</strong></em>  to prevent water from standing for several days.</li>
				<li>If ditches do not flow and contain stagnant water for one week or longer, they can produce large numbers of mosquitoes. 
				Report such conditions to a Mosquito Control or Public Health Office. Do not attempt to clear these ditches because they 
				may be protected by wetland regulations.</li>
			</ol>
